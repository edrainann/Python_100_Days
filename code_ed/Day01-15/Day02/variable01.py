a = 7
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)  # 除法
print('a // b =', a // b)  # 地板除。整除，//除法不管操作数为何种数值类型，总是舍弃小数部分。取整除 - 返回商的整数部分（向下取整）
print('a % b = ', a % b)  # 取余数。查看是否能整除,若能除尽 值为0
print(a ** b)
